# года рождения
birth_year = 2003
current_year = 2024

# вычисление возраста через 2 года
age_in_future = current_year - birth_year + 2

# вычисление возраста два года назад
age_two_years_ago = current_year - birth_year - 2

# проверка и вывод результатов через if
if age_in_future > age_two_years_ago:
    print("Через 2 года вам будет больше, чем два года назад.")
elif age_in_future < age_two_years_ago:
    print("Через 2 года вам будет меньше, чем два года назад.")
else:
    print("Через 2 года вам будет столько же, сколько два года назад.")



